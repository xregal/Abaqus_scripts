C**********************************************************
C    VUMAT FOR STRAIN RATE DEPENDENT STIFFNESS MODEL, ORTHOTROPIC MATERIELS
C    WRITTEN BY HAIBIN ZHU
C**********************************************************
      SUBROUTINE VUMAT(
C READ ONLY (UNMODIFIABLE)VARIABLES -
     1  NBLOCK, NDIR, NSHR, NSTATEV, NFIELDV, NPROPS, LANNEAL,
     2  STEPTIME, TOTALTIME, DT, CMNAME, COORDMP, CHARLENGTH,
     3  PROPS, DENSITY, STRAININC, RELSPININC,
     4  TEMPOLD, STRETCHOLD, DEFGRADOLD, FIELDOLD,
     5  STRESSOLD, STATEOLD, ENERINTERNOLD, ENERINELASOLD,
     6  TEMPNEW, STRETCHNEW, DEFGRADNEW, FIELDNEW,
C WRITE ONLY (MODIFIABLE) VARIABLES -
     7  STRESSNEW, STATENEW, ENERINTERNNEW, ENERINELASNEW )
C
      INCLUDE 'VABA_PARAM.INC'
C
      DIMENSION PROPS(NPROPS), DENSITY(NBLOCK), COORDMP(NBLOCK,*),
     1  CHARLENGTH(NBLOCK), STRAININC(NBLOCK,NDIR+NSHR),
     2  RELSPININC(NBLOCK,NSHR), TEMPOLD(NBLOCK),
     3  STRETCHOLD(NBLOCK,NDIR+NSHR),
     4  DEFGRADOLD(NBLOCK,NDIR+NSHR+NSHR),
     5  FIELDOLD(NBLOCK,NFIELDV), STRESSOLD(NBLOCK,NDIR+NSHR),
     6  STATEOLD(NBLOCK,NSTATEV), ENERINTERNOLD(NBLOCK),
     7  ENERINELASOLD(NBLOCK), TEMPNEW(NBLOCK),
     8  STRETCHNEW(NBLOCK,NDIR+NSHR),
     8  DEFGRADNEW(NBLOCK,NDIR+NSHR+NSHR),
     9  FIELDNEW(NBLOCK,NFIELDV),
     1  STRESSNEW(NBLOCK,NDIR+NSHR), STATENEW(NBLOCK,NSTATEV),
     2  ENERINTERNNEW(NBLOCK), ENERINELASNEW(NBLOCK)
C
      CHARACTER*80 CMNAME
C
      PARAMETER( ZERO = 0.D0, ONE = 1.D0, TWO = 2.D0, THREE = 3.D0,
     1  THIRD = ONE/THREE, HALF = .5D0, TWOTHIRDS = TWO/THREE,
     2  THREEHALFS = 1.5D0 )
      Q11  = PROPS(1)
      Q12  = PROPS(2)
      Q13  = PROPS(3)
      Q23  = PROPS(4)
      Q33  = PROPS(5)      
      
      A2  = 75E8
      B2  = 6E8
      A6  = 4E9
      B6  = 3.5E8

      DO NP = 1, NBLOCK
C Copy the values of strains into temp variables

      ES1 = strainInc(NP,1)
      ES2 = strainInc(NP,2)
      ES3 = strainInc(NP,3)
      ES4 = strainInc(NP,4)
      

C     COMPUTE THE STRAIN RATE DEPEDENT STIFFNESS 
C     THE SHEAR STRAIN COMPONENT IS TENSORIAL STRAIN RATHER THAN ENGINEERING SHEAR STRAIN

C     TRANSVERSE STRAIN RATE
      SR2=1.*ABS(strainInc(NP,2)/DT)

C     SHEAR STRAIN RATE (ENGINEERING SHEAR STRAIN)
      SR4=2.*ABS(strainInc(NP,4)/DT)

C     STRAIN RATE EFFECT ON TRANSVERSE COMPONENT
      Q22 = A2+B2*LOG(SR2+1)  
      Q66 = A6+B6*LOG(SR4+1)  
C      Q66=A6
      
     
      stressNew(NP,1) = stressOld(NP,1)+Q11*ES1+Q12*ES2+Q13*ES3
      stressNew(NP,2) = stressOld(NP,2)+Q12*ES1+Q22*ES2+Q23*ES3
      stressNew(NP,3) = stressOld(NP,3)+Q13*ES1+Q23*ES2+Q33*ES3
      stressNew(NP,4) = stressOld(NP,4)+Q66*ES4*2.

C     CHECK THE TIME INCREMENT    
      write(6,*) DT

      END DO
     
      RETURN
      END